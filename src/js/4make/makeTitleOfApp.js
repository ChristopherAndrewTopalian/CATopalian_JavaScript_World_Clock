function makeTitleOfApp()
{
    let theLink = ce('a');
    theLink.textContent = 'CATopalian JavaScript World Clock';
    theLink.href = 'https://github.com/ChristopherAndrewTopalian/CATopalian_JavaScript_World_Clock';
    theLink.target = '_blank';
    theLink.style.fontFamily = 'Arial';
    theLink.style.fontWeight = 'bold';
    theLink.style.fontSize = '17px';
    theLink.style.color = 'rgb(170, 170, 170)';
    theLink.style.textDecoration = 'none';
    return theLink;
}

